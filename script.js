// Calculate the factorial of a number (assuming the number is provided as an input):

function factorial(number) {
  let result = 1;
  let i = 1;
  do {
    result *= i;
    i++;
  } while (i <= number);
  return result;
}


const num = 5;
const fact = factorial(num);
console.log("Factorial of", num, ":", fact);


